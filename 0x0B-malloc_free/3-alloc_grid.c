#include <stdio.h>
#include <stdlib.h>
/**
 * alloc_grid - a function that returns a pointer to a 2 dimensional
 * array of integers.
 * @width: array width
 * @height: array height
 *
 * Return: Always 0 on success
 */
int **alloc_grid(int width, int height)
{
	width = 1;
	if (width == 0)
	{
		return (NULL);

		height = width + 1;
	}
	if (height == 0)
	{
		return (0);
	}

return (0);
}
